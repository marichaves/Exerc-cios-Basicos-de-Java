package exercicios;

public class Imprime {

	public static void main(String[] args) {
		//Imprima de 1 a 20, um abaixo do outro
		
		for (int i = 1; i <= 20; i++ ) {
			System.out.println(i);
		}
		//Imprima de 1 a 20, um ao lado do outro
		for (int i = 1; i <= 20; i++ ) {
			System.out.print(i +" ");
		}

	}

}
